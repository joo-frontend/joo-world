    <script>
        $(document).ready(function () {
            /*header*/
            $(".gnb li").removeClass("focus");

        var menu = ['01', '02', '03', '04']
        var swiper = new Swiper('#mainPage', {
            direction: 'vertical',
            on: {
                transitionEnd: function () {
                    if ($(".swiper-slide-active").index() == 0) {
                        $(".header_white_wra .right_menuBox").removeClass("active");
                        $(".header_white_wra .left_menuBox").removeClass("active");
                        $(".main_pagination").removeClass("black");
                    }
                    if ($(".swiper-slide-active").index() == 1) {
                        $(".main_pagination").addClass("black");
                        $(".header_white_wra .left_menuBox").addClass("active");
                        $(".header_white_wra .right_menuBox").addClass("active");
                    }
                    if ($(".swiper-slide-active").index() == 2) {
                        $(".main_pagination").removeClass("black");
                        $(".header_white_wra .left_menuBox").addClass("active");
                        $(".header_white_wra .right_menuBox").addClass("active");
                    }
                    if ($(".swiper-slide-active").index() == 3) {
                        $(".main_pagination").removeClass("black");
                        $(".header_white_wra .right_menuBox").addClass("active");
                        $(".header_white_wra .left_menuBox").removeClass("active");
                    }
                },
            },
            pagination: {
                el: '.main_pagination',
                clickable: true,
                renderBullet: function (index, className) {
                    return '<span class="' + className + '">' + (menu[index]) + '</span>';
                },
            },
            simulateTouch: false,
            mousewheel: {},
            spaceBetween: 0,
        });
        });

        $(document).scroll(function () {
            var top = $(window).scrollTop();
            function menuFocus(n){
                $(".flex .main_pagination li").removeClass("focus");
                $(".flex .main_pagination li.page"+n).addClass("focus");
            }
            var mainP1 = $("#mainP1");
            var mainP2 = $("#mainP2");
            var mainP3 = $("#mainP3");
            var mainP4 = $("#mainP4");
            var scrollHeight = $(document).height();
            var scrollPosition = $(window).height() + $(window).scrollTop();
            if((scrollHeight - scrollPosition) / scrollHeight === 0) {
                menuFocus(4);
            } else if(top >= mainP1.offset().top){
                menuFocus(1);
                if(top >= mainP2.offset().top){
                    menuFocus(2);
                    if(top >= mainP3.offset().top){
                        menuFocus(3);
                        if(top >= mainP4.offset().top){
                            menuFocus(4);
                        }
                    }
                }
            }

        });


    </script>
