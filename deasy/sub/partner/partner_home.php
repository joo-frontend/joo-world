    function fieldbox(field,contents) {
        $(".choice_field").append("<div class='choice_item' data-choice='"+field+"'><p>"+contents+"</p><i><img src='/assets/images/bidding/delete.png' alt=''></i></div>");
        $(".choice_item img").click(function () {
            var xData = $(this).parents(".choice_item").attr("data-choice");

            removeBox(xData);
            $(".fieldset1 .type[data-field='"+xData+"']").removeClass("focus");
            $(".fieldset2 .type[data-field='"+xData+"']").removeClass("focus");
            if(xData == "field1"){
                $(".fieldset1 li").removeClass("focus");
            }else if(xData == 'field2'){
                $(".fieldset2 li").removeClass("focus");
            }
        });
    }
    function removeFocus(typeR) {
        typeR.removeClass("focus");
    }
    function addFocus(typeR) {
        typeR.addClass("focus");
    }
    function removeBox(dataChoice) {
        $(".choice_field .choice_item[data-choice='"+dataChoice+"']").remove();
    }
    /*field1*/
    $(".fieldset1 .type").click(function () {
        var choiceType = $(this);
        var choiceData = $(this).attr("data-field");
        var choiceTxt = $(this).find("p").html();
        if(choiceType.hasClass("focus") == false){
            addFocus(choiceType);
            fieldbox(choiceData, choiceTxt);
        }else {
            removeFocus(choiceType);
            removeBox(choiceData);
        }
        var all1T = $(".fieldset1 .all_type");
        if(all1T.hasClass("focus") == true){
            all1T.removeClass("focus");
            removeFocus(all1T);
            removeBox("field1");
            $(".fieldset1 .type").each(function () {
                if($(this).hasClass("focus") == true) {
                    var restData = $(this).attr("data-field");
                    var txtRest = $(this).find("p").html();
                    fieldbox(restData, txtRest);
                }
            });
        }
        var typeNum = $(".fieldset1 .type").length;
        var typeNumF = $(".fieldset1 .type.focus").length;
        if(typeNumF == typeNum){
            fieldbox("field1", "분야전체");
            for(n=1; n<=typeNum; n++){
                removeBox("field1_"+n);
            }
            addFocus(all1T);
        }
    });
    $(".fieldset1 .all_type").click(function () {
        var choiceType = $(this);
        var itemBox = $(".fieldset1 .type").length ;
        if(choiceType.hasClass("focus") == false){
            addFocus(choiceType);
            for(i=0; i<=itemBox; i++) {
                removeBox("field1_"+i);
            }
            fieldbox("field1","분야전체");
            $(".fieldset1 .type").addClass("focus");
        }else {
            removeFocus(choiceType);
            for(i=0; i<=itemBox; i++) {
                removeBox("field1_"+i);
            }
            removeBox("field1");
            $(".fieldset1 .type").removeClass("focus");
        }
    });
